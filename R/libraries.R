#  Data Manipulation & Cleaning ----
library(dplyr)        # Core data wrangling (mutate, filter, summarise, etc.)
library(tidyr)        # Data reshaping (pivot_wider, pivot_longer, spread, gather)
library(readxl)       # Read Excel files (.xls, .xlsx)
library(openxlsx)     # Write Excel files (.xlsx)
library(tidyverse)    # Collection of core tidy tools (includes ggplot2, dplyr, tidyr, etc.)


#  Statistical Modeling & Analysis  ----
library(afex)         # ANOVA and mixed models; wrapper around lme4
library(lme4)         # Linear and generalized linear mixed-effects models
library(emmeans)      # Estimated marginal means (post-hoc comparisons)
library(effectsize)   # Effect size computations (Cohen's d, eta², etc.)

# Data Visualization  ----
library(ggplot2)      # Core plotting system (grammar of graphics)
